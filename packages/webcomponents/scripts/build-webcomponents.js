import fs from 'fs-extra';
import path from 'path';
import { createRequire } from 'module';

const require = createRequire(import.meta.url);

const ICONS_PKG_DIR = path.dirname(
  require.resolve('@lets-ui/icons/package.json')
);
const ICONS_SRC = path.join(ICONS_PKG_DIR, 'dist');
const CATEGORIES_FILE = path.join(ICONS_PKG_DIR, 'categories.json');
const OUT_DIR = 'dist';
const COMPONENTS_DIR = path.join(OUT_DIR, 'components');

const HEADER = `// Autogenerated by Let's UI Icons. DO NOT EDIT!\n`;

function toClassName(name) {
  return (
    name
      .split('-')
      .map((p) => p[0].toUpperCase() + p.slice(1))
      .join('') + 'Icon'
  );
}

function componentModule(name, variants) {
  const tagName = `lui-${name}`;
  const className = toClassName(name);
  const outline = (variants.outline ?? '').replace(/`/g, '\\`');
  const solid = (variants.solid ?? '').replace(/`/g, '\\`');

  return `${HEADER}class ${className} extends HTMLElement {
  static get observedAttributes() { return ['solid']; }
  connectedCallback() { this._render(); }
  attributeChangedCallback() { this._render(); }
  _render() {
    this.innerHTML = this.hasAttribute('solid')
      ? \`${solid || outline}\`
      : \`${outline}\`;
  }
}
customElements.define('${tagName}', ${className});
`;
}

async function run() {
  await fs.ensureDir(COMPONENTS_DIR);

  const files = await fs.readdir(ICONS_SRC);
  const svgs = files.filter((f) => f.endsWith('.svg'));

  const allIcons = {};

  for (const file of svgs) {
    const isSolid = file.endsWith('-solid.svg');
    const name = file.replace('-solid.svg', '').replace('.svg', '');
    const svg = await fs.readFile(path.join(ICONS_SRC, file), 'utf8');
    if (!allIcons[name]) allIcons[name] = {};
    allIcons[name][isSolid ? 'solid' : 'outline'] = svg;
  }

  const categories = JSON.parse(await fs.readFile(CATEGORIES_FILE, 'utf8'));

  const iconCategories = {};
  for (const [cat, list] of Object.entries(categories)) {
    for (const name of list) {
      if (!iconCategories[name]) iconCategories[name] = [];
      iconCategories[name].push(cat);
    }
  }

  // One JS module per icon — SVG lives here and only here
  for (const [name, variants] of Object.entries(allIcons)) {
    await fs.writeFile(
      path.join(COMPONENTS_DIR, `${name}.js`),
      componentModule(name, variants)
    );
  }

  // Group icon names by category
  const grouped = {};
  for (const name of Object.keys(allIcons)) {
    const cats = iconCategories[name] ?? ['misc'];
    for (const cat of cats) {
      if (!grouped[cat]) grouped[cat] = [];
      grouped[cat].push(name);
    }
  }

  // {category}.js — imports only its components
  const categoryNames = Object.keys(grouped);
  for (const cat of categoryNames) {
    const imports = grouped[cat]
      .map((name) => `import './components/${name}.js';`)
      .join('\n');
    await fs.writeFile(
      path.join(OUT_DIR, `${cat}.js`),
      HEADER + imports + '\n'
    );
    console.log(`✔ ${cat}.js (${grouped[cat].length} icons)`);
  }

  // index.js — imports every component directly
  const allImports = Object.keys(allIcons)
    .map((name) => `import './components/${name}.js';`)
    .join('\n');
  await fs.writeFile(
    path.join(OUT_DIR, 'index.js'),
    HEADER + allImports + '\n'
  );

  console.log(
    `✅ Web components generated (${Object.keys(allIcons).length} icons)`
  );
}

run().catch((err) => {
  console.error('❌ Error generating web components');
  console.error(err);
});
