import fs from 'fs-extra';
import path from 'path';
import { createRequire } from 'module';

const require = createRequire(import.meta.url);

const ICONS_PKG_DIR = path.dirname(
  require.resolve('@lets-ui/icons/package.json')
);
const ICONS_SRC = path.join(ICONS_PKG_DIR, 'dist');
const OUT_DIR = 'dist';
const OUT_FILE = path.join(OUT_DIR, 'index.js');

async function run() {
  await fs.ensureDir(OUT_DIR);

  const files = await fs.readdir(ICONS_SRC);
  const svgs = files.filter((f) => f.endsWith('.svg'));

  const icons = {};

  for (const file of svgs) {
    const isSolid = file.endsWith('-solid.svg');
    const name = file.replace('-solid.svg', '').replace('.svg', '');
    const svg = await fs.readFile(path.join(ICONS_SRC, file), 'utf8');

    if (!icons[name]) icons[name] = {};
    icons[name][isSolid ? 'solid' : 'outline'] = svg;
  }

  const components = Object.entries(icons)
    .map(([name, variants]) => {
      const tagName = `lui-${name}`;
      const className = name
        .split('-')
        .map((p) => p[0].toUpperCase() + p.slice(1))
        .join('');

      const outlineSvg = variants.outline
        ? variants.outline.replace(/`/g, '\\`')
        : '';
      const solidSvg = variants.solid
        ? variants.solid.replace(/`/g, '\\`')
        : '';

      return `
class ${className}Icon extends HTMLElement {
  static get observedAttributes() { return ['solid']; }

  connectedCallback() { this._render(); }
  attributeChangedCallback() { this._render(); }

  _render() {
    const solid = this.hasAttribute('solid');
    this.innerHTML = solid
      ? \`${solidSvg || outlineSvg}\`
      : \`${outlineSvg}\`;
  }
}
customElements.define('${tagName}', ${className}Icon);
`;
    })
    .join('');

  const output = `// Autogenerated by Let's UI Icons. DO NOT EDIT!\n${components}`;

  await fs.writeFile(OUT_FILE, output);
  console.log(
    `✅ Web components generated (${Object.keys(icons).length} icons)`
  );
}

run().catch((err) => {
  console.error('❌ Error generating web components');
  console.error(err);
});
